def parse_list_like_cell(value: Any) -> List[Any]:
    """
    Parses values such as:
      ["123", "456"]
      ['123', '456']
      123,456
      123 | 456
      scalar
    Empty scalar cells return an empty list.
    """
    if value is None:
        return []
    if isinstance(value, (list, tuple)):
        return list(value)
    if isinstance(value, pd.Series):
        return list(value.values)
    if isinstance(value, (datetime, date, pd.Timestamp)):
        return [value]
    if isinstance(value, (int, float, Decimal)):
        if is_missing(value):
            return []
        return [value]

    s = clean_scalar_text(value)
    if not s or s == "[]":
        return []

    if s.startswith("[") and s.endswith("]"):
        try:
            parsed = json.loads(s)
            if isinstance(parsed, list):
                return parsed
        except Exception:
            pass
        try:
            parsed = ast.literal_eval(s)
            if isinstance(parsed, list):
                return parsed
        except Exception:
            pass
        inner = s[1:-1].strip()
        if not inner:
            return []
        return [p.strip().strip("'\"") for p in inner.split(",")]

    if " | " in s:
        return [p.strip() for p in s.split(" | ")]

    if "," in s and not re.search(r"\d{1,2}/\d{1,2}/\d{2,4}", s):
        return [p.strip() for p in s.split(",")]

    return [s]


def parse_numeric_value(value: Any) -> Optional[float]:
    if is_missing(value):
        return None
    if isinstance(value, Decimal):
        return float(value)
    if isinstance(value, (int, float)):
        try:
            if isinstance(value, float) and math.isnan(value):
                return None
            return float(value)
        except Exception:
            return None
    s = clean_scalar_text(value)
    if not s:
        return None
    s = s.strip().strip("'\"")
    s = s.replace("$", "").replace(",", "")
    try:
        return float(Decimal(s))
    except (InvalidOperation, ValueError):
        return None


def parse_integer_value(value: Any) -> Optional[int]:
    if is_missing(value):
        return None
    if isinstance(value, bool):
        return int(value)
    if isinstance(value, int):
        return int(value)
    s = clean_scalar_text(value).strip().strip("'\"")
    if not s:
        return None
    s = s.replace(",", "")
    if re.fullmatch(r"-?\d+", s):
        return int(s)
    if re.fullmatch(r"-?\d+\.0+", s):
        return int(float(s))
    num = parse_numeric_value(s)
    if num is None:
        return None
    try:
        return int(Decimal(str(num)).to_integral_value())
    except Exception:
        return None


def parse_date_value(value: Any) -> Optional[str]:
    """Return ISO date string YYYY-MM-DD, or None."""
    if is_missing(value):
        return None

    if isinstance(value, pd.Timestamp):
        if pd.isna(value):
            return None
        return value.date().isoformat()
    if isinstance(value, datetime):
        return value.date().isoformat()
    if isinstance(value, date):
        return value.isoformat()

    if isinstance(value, (int, float)):
        try:
            if isinstance(value, float) and math.isnan(value):
                return None
            if 20000 <= float(value) <= 80000:
                return pd.to_datetime(value, unit="D", origin="1899-12-30").date().isoformat()
        except Exception:
            pass

    s = clean_scalar_text(value).strip().strip("'\"")
    if not s:
        return None
    s = re.sub(r"T\d{2}:\d{2}:\d{2}(?:\.\d+)?Z?$", "", s)

    for fmt in ("%m/%d/%Y", "%m/%d/%y", "%Y-%m-%d", "%Y/%m/%d", "%m-%d-%Y", "%m-%d-%y"):
        try:
            return datetime.strptime(s, fmt).date().isoformat()
        except ValueError:
            pass

    try:
        parsed = pd.to_datetime(s, errors="coerce")
        if pd.isna(parsed):
            return None
        return parsed.date().isoformat()
    except Exception:
        return None


def parse_typed_list(value: Any, field_name: str) -> List[Any]:
    raw_items = parse_list_like_cell(value)
    parsed: List[Any] = []
    for item in raw_items:
        if field_name in {"acres_list", "market_value_list"}:
            parsed.append(parse_numeric_value(item))
        elif field_name in {"owner_tax_year_list", "id_list"}:
            parsed.append(parse_integer_value(item))
        elif field_name == "deed_date_list":
            parsed.append(parse_date_value(item))
        else:
            parsed.append(None if is_missing(item) else item)
    return parsed


def pad_or_trim(values: List[Any], length: int) -> Tuple[List[Any], bool, bool]:
    padded = False
    trimmed = False
    if length <= 0:
        return values, False, False
    if len(values) < length:
        values = values + [None] * (length - len(values))
        padded = True
    elif len(values) > length:
        values = values[:length]
        trimmed = True
    return values, padded, trimmed


def non_null_count(values: Sequence[Any]) -> int:
    return sum(1 for v in values if v is not None and not is_missing(v))


def normalize_land_id_text(value: Any) -> str:
    s = clean_scalar_text(value)
    if not s:
        return ""
    # Excel sometimes stores IDs as 10522.0.
    if re.fullmatch(r"\d+\.0+", s):
        return str(int(float(s)))
    return s


def normalize_name(value: Any) -> str:
    s = clean_scalar_text(value).lower()
    s = re.sub(r"\s+", " ", s)
    return s.strip()


def extract_ma_hash(value: str) -> str:
    m = re.search(r"\bMA#[A-Z0-9]+\b", clean_scalar_text(value).upper())
    return m.group(0) if m else ""


