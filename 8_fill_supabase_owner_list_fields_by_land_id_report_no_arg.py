def process_county(
    client: Client,
    county_folder: Path,
    county_meta: CountyMeta,
) -> Tuple[List[CountyTableReportRow], List[SkippedRow], List[ProposedUpdate]]:
    county_name = county_folder.name
    sources, source_skips, source_stats = read_and_prepare_county_sources(county_folder, county_name)

    log(
        f"[SOURCE] {county_name}: files={source_stats.get('files_read', 0):,} "
        f"rows={source_stats.get('source_rows', 0):,} prepared={len(sources):,} "
        f"skipped_source={len(source_skips):,} length_mismatch={source_stats.get('source_rows_with_length_mismatch', 0):,}"
    )

    reports: List[CountyTableReportRow] = []
    all_skips: List[SkippedRow] = list(source_skips)
    all_updates: List[ProposedUpdate] = []

    if not sources:
        for table_name in TARGET_TABLES:
            reports.append(CountyTableReportRow(
                county_name=county_name,
                table_name=table_name,
                files_read=source_stats.get("files_read", 0),
                source_rows=source_stats.get("source_rows", 0),
                prepared_sources=0,
                source_rows_missing_land_id=source_stats.get("source_rows_missing_land_id", 0),
                source_rows_without_valid_ids=source_stats.get("source_rows_without_valid_ids", 0),
                source_rows_with_length_mismatch=source_stats.get("source_rows_with_length_mismatch", 0),
                db_rows_fetched=0,
                matched_sources=0,
                unmatched_sources=0,
                ambiguous_sources=0,
                no_change_matches=0,
                duplicate_update_conflicts=0,
                proposed_updates=0,
                applied_updates=0,
                ids_added_to_db_lists=0,
                id_list_updates=0,
                acres_list_updates=0,
                market_value_updates=0,
                owner_tax_year_updates=0,
                deed_date_updates=0,
                non_null_acres=0,
                non_null_market_values=0,
                non_null_owner_tax_years=0,
                non_null_deed_dates=0,
                status="warn",
                error="no_prepared_source_rows",
            ))
        return reports, source_skips, all_updates

    for table_name in TARGET_TABLES:
        log("-" * 90)
        log(f"[TABLE] {county_name} -> {table_name}")
        report, skips, updates = process_table_for_county(
            client=client,
            county_meta=county_meta,
            county_name=county_name,
            table_name=table_name,
            sources=sources,
            source_skips=source_skips,
            source_stats=source_stats,
        )
        reports.append(report)
        all_skips.extend(skips)
        all_updates.extend(updates)

    return reports, all_skips, all_updates


