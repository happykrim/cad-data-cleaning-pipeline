def write_csv_reports(
    county_table_reports: List[CountyTableReportRow],
    skipped_rows: List[SkippedRow],
    proposed_updates: List[ProposedUpdate],
) -> None:
    ensure_folder(REPORT_FOLDER)

    pd.DataFrame([asdict(r) for r in county_table_reports]).to_csv(SUMMARY_CSV_PATH, index=False)
    log(f"[REPORT] Summary CSV written: {SUMMARY_CSV_PATH}")

    if skipped_rows:
        pd.DataFrame([asdict(r) for r in skipped_rows]).to_csv(SKIPPED_ROWS_CSV_PATH, index=False)
    else:
        pd.DataFrame(columns=[f.name for f in SkippedRow.__dataclass_fields__.values()]).to_csv(SKIPPED_ROWS_CSV_PATH, index=False)
    log(f"[REPORT] Skipped-row audit CSV written: {SKIPPED_ROWS_CSV_PATH}")

    audit_rows = []
    for u in proposed_updates:
        row = asdict(u)
        # Keep audit CSV readable; payload can be large.
        row["payload_fields"] = json_dumps_compact(u.payload_fields)
        audit_rows.append(row)
    pd.DataFrame(audit_rows).to_csv(UPDATE_AUDIT_CSV_PATH, index=False)
    log(f"[REPORT] Update audit CSV written: {UPDATE_AUDIT_CSV_PATH}")


