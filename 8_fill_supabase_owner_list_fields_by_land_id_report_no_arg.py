def get_raw_list_for_source_field(row: pd.Series, field: str, raw_id_len: int) -> List[Any]:
    if field in row.index:
        values = parse_list_like_cell(row.get(field))
    else:
        values = []

    if field == "acres_list" and not values and SOURCE_SCALAR_ACRES_COLUMN in row.index:
        scalar_acres = row.get(SOURCE_SCALAR_ACRES_COLUMN)
        if not is_missing(scalar_acres):
            values = [scalar_acres]

    values, _, _ = pad_or_trim(values, raw_id_len)
    return values


def dedupe_source_lists_by_id(
    ids: List[int],
    acres: List[Optional[float]],
    market: List[Optional[float]],
    tax_year: List[Optional[int]],
    deed_date: List[Optional[str]],
) -> Tuple[List[int], List[Optional[float]], List[Optional[float]], List[Optional[int]], List[Optional[str]]]:
    order: List[int] = []
    data: Dict[int, Dict[str, Any]] = {}
    for idx, parcel_id in enumerate(ids):
        if parcel_id not in data:
            order.append(parcel_id)
            data[parcel_id] = {
                "acres_list": acres[idx] if idx < len(acres) else None,
                "market_value_list": market[idx] if idx < len(market) else None,
                "owner_tax_year_list": tax_year[idx] if idx < len(tax_year) else None,
                "deed_date_list": deed_date[idx] if idx < len(deed_date) else None,
            }
        else:
            # Keep first non-empty value per field.
            if data[parcel_id]["acres_list"] is None and idx < len(acres):
                data[parcel_id]["acres_list"] = acres[idx]
            if data[parcel_id]["market_value_list"] is None and idx < len(market):
                data[parcel_id]["market_value_list"] = market[idx]
            if data[parcel_id]["owner_tax_year_list"] is None and idx < len(tax_year):
                data[parcel_id]["owner_tax_year_list"] = tax_year[idx]
            if data[parcel_id]["deed_date_list"] is None and idx < len(deed_date):
                data[parcel_id]["deed_date_list"] = deed_date[idx]

    return (
        order,
        [data[i]["acres_list"] for i in order],
        [data[i]["market_value_list"] for i in order],
        [data[i]["owner_tax_year_list"] for i in order],
        [data[i]["deed_date_list"] for i in order],
    )


def prepare_source_record(
    row: pd.Series,
    land_id_col: str,
    county_name: str,
    source_file: str,
    source_row_number: int,
) -> Tuple[Optional[SourceRecord], Optional[SkippedRow]]:
    source_land_id = normalize_land_id_text(row.get(land_id_col))
    if not source_land_id:
        return None, SkippedRow(
            county_name=county_name,
            table_name="source_file",
            source_file=source_file,
            source_row_number=source_row_number,
            source_land_id="",
            source_id_list="",
            reason="missing_source_land_id",
            details=f"Expected one of {SOURCE_LAND_ID_COLUMN_CANDIDATES}; using column {land_id_col}.",
        )

    raw_id_items = parse_list_like_cell(row.get(SOURCE_ID_LIST_COLUMN)) if SOURCE_ID_LIST_COLUMN in row.index else []
    if not raw_id_items:
        raw_id_items = [source_land_id]

    raw_id_len = len(raw_id_items)
    valid_positions: List[int] = []
    parsed_ids: List[int] = []
    invalid_id_items = 0
    for pos, item in enumerate(raw_id_items):
        parsed_id = parse_integer_value(item)
        if parsed_id is None:
            invalid_id_items += 1
            continue
        valid_positions.append(pos)
        parsed_ids.append(parsed_id)

    if not parsed_ids:
        source_as_int = parse_integer_value(source_land_id)
        if source_as_int is not None:
            raw_id_items = [source_land_id]
            raw_id_len = 1
            valid_positions = [0]
            parsed_ids = [source_as_int]
        else:
            return None, SkippedRow(
                county_name=county_name,
                table_name="source_file",
                source_file=source_file,
                source_row_number=source_row_number,
                source_land_id=source_land_id,
                source_id_list=json_dumps_compact(raw_id_items),
                reason="no_valid_parcel_ids",
                details="Neither id_list nor source land_id contained a valid integer parcel id.",
            )

    padded_fields: List[str] = []
    trimmed_fields: List[str] = []
    length_mismatch = invalid_id_items > 0

    field_raw: Dict[str, List[Any]] = {}
    for field in VALUE_LIST_FIELDS:
        raw_values = get_raw_list_for_source_field(row, field, raw_id_len)
        original_len = len(parse_list_like_cell(row.get(field))) if field in row.index else 0
        if field == "acres_list" and original_len == 0 and SOURCE_SCALAR_ACRES_COLUMN in row.index and not is_missing(row.get(SOURCE_SCALAR_ACRES_COLUMN)):
            original_len = 1
        if original_len and original_len != raw_id_len:
            length_mismatch = True
            if original_len < raw_id_len:
                padded_fields.append(field)
            elif original_len > raw_id_len:
                trimmed_fields.append(field)
        field_raw[field] = raw_values

    acres = [parse_numeric_value(field_raw["acres_list"][pos]) for pos in valid_positions]
    market = [parse_numeric_value(field_raw["market_value_list"][pos]) for pos in valid_positions]
    tax_year = [parse_integer_value(field_raw["owner_tax_year_list"][pos]) for pos in valid_positions]
    deed_date = [parse_date_value(field_raw["deed_date_list"][pos]) for pos in valid_positions]

    parsed_ids, acres, market, tax_year, deed_date = dedupe_source_lists_by_id(
        parsed_ids, acres, market, tax_year, deed_date
    )

    full_name = clean_scalar_text(row.get(SOURCE_FULL_NAME_COLUMN)) if SOURCE_FULL_NAME_COLUMN in row.index else ""
    lands_count = parse_integer_value(row.get(SOURCE_LANDS_COUNT_COLUMN)) if SOURCE_LANDS_COUNT_COLUMN in row.index else None

    return SourceRecord(
        county_name=county_name,
        source_file=source_file,
        source_row_number=source_row_number,
        source_land_id=source_land_id,
        full_name=full_name,
        lands_count=lands_count,
        id_list=parsed_ids,
        acres_list=acres,
        market_value_list=market,
        owner_tax_year_list=tax_year,
        deed_date_list=deed_date,
        length_mismatch=length_mismatch,
        padded_fields=", ".join(sorted(set(padded_fields))),
        trimmed_fields=", ".join(sorted(set(trimmed_fields))),
        invalid_id_items=invalid_id_items,
    ), None


def read_and_prepare_county_sources(
    county_folder: Path,
    county_name: str,
) -> Tuple[List[SourceRecord], List[SkippedRow], Dict[str, int]]:
    files = list_data_files(county_folder)
    stats = {
        "files_read": 0,
        "source_rows": 0,
        "prepared_sources": 0,
        "source_rows_missing_land_id": 0,
        "source_rows_without_valid_ids": 0,
        "source_rows_with_length_mismatch": 0,
    }
    sources: List[SourceRecord] = []
    skipped: List[SkippedRow] = []

    if not files:
        log(f"[WARN] No data files found in county folder: {county_folder}")
        return sources, skipped, stats

    for file_path in files:
        log(f"[FILE] Reading {county_name}: {file_path.name}")
        df = read_data_file(file_path)
        stats["files_read"] += 1
        stats["source_rows"] += len(df)

        land_id_col = find_column(df, SOURCE_LAND_ID_COLUMN_CANDIDATES)
        if not land_id_col:
            log(f"[WARN] {file_path.name}: missing source land id column. Expected {SOURCE_LAND_ID_COLUMN_CANDIDATES}")
            for row_number in range(2, len(df) + 2):
                skipped.append(SkippedRow(
                    county_name=county_name,
                    table_name="source_file",
                    source_file=file_path.name,
                    source_row_number=row_number,
                    source_land_id="",
                    source_id_list="",
                    reason="missing_source_land_id_column",
                    details=f"Expected one of {SOURCE_LAND_ID_COLUMN_CANDIDATES}",
                ))
            stats["source_rows_missing_land_id"] += len(df)
            continue

        missing_optional = [c for c in UPDATE_LIST_FIELDS if c not in df.columns]
        if missing_optional:
            log(f"[WARN] {file_path.name}: missing optional update columns: {missing_optional}")

        for idx, row in df.iterrows():
            source_row_number = int(idx) + 2
            source, skip = prepare_source_record(
                row=row,
                land_id_col=land_id_col,
                county_name=county_name,
                source_file=file_path.name,
                source_row_number=source_row_number,
            )
            if skip is not None:
                skipped.append(skip)
                if skip.reason in {"missing_source_land_id", "missing_source_land_id_column"}:
                    stats["source_rows_missing_land_id"] += 1
                elif skip.reason == "no_valid_parcel_ids":
                    stats["source_rows_without_valid_ids"] += 1
                continue
            if source is None:
                continue

            sources.append(source)
            stats["prepared_sources"] += 1
            stats["source_rows_with_length_mismatch"] += int(source.length_mismatch)

            if TEST_ROW_LIMIT is not None and len(sources) >= TEST_ROW_LIMIT:
                log(f"[TEST] Reached TEST_ROW_LIMIT={TEST_ROW_LIMIT:,} while reading {county_name}.")
                return sources, skipped, stats

    return sources, skipped, stats


