def build_db_indices(db_rows: List[DbOwnerRow]) -> Tuple[Dict[str, List[DbOwnerRow]], Dict[int, List[DbOwnerRow]]]:
    by_land_id: Dict[str, List[DbOwnerRow]] = {}
    by_parcel_id: Dict[int, List[DbOwnerRow]] = {}

    for row in db_rows:
        if row.land_id:
            by_land_id.setdefault(row.land_id, []).append(row)
            land_id_int = parse_integer_value(row.land_id)
            if land_id_int is not None and not row.id_list:
                # If id_list is not populated yet, still allow matching by the row land_id.
                by_parcel_id.setdefault(land_id_int, []).append(row)

        for parcel_id in row.id_list:
            by_parcel_id.setdefault(parcel_id, []).append(row)

    return by_land_id, by_parcel_id


