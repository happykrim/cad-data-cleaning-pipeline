def name_score(source_name: str, db_name: str) -> int:
    src = normalize_name(source_name)
    db = normalize_name(db_name)
    if not src or not db:
        return 0
    if src == db:
        return 30

    src_hash = extract_ma_hash(source_name)
    db_hash = extract_ma_hash(db_name)
    if src_hash and db_hash and src_hash == db_hash:
        return 25

    # Small bonus when the primary owner token is clearly contained.
    src_primary = src.split("|")[-1].strip() if "|" in src else src
    src_primary = re.sub(r"\(\+\d+\s+others?\)", "", src_primary).strip()
    if src_primary and (src_primary in db or db in src_primary):
        return 10
    return 0


def score_candidate(source: SourceRecord, db_row: DbOwnerRow) -> Tuple[int, str, int]:
    methods: List[str] = []
    score = 0

    source_land_id = source.source_land_id
    source_land_id_int = parse_integer_value(source_land_id)
    source_ids = set(source.id_list)
    db_ids = set(db_row.id_list)
    db_land_id_int = parse_integer_value(db_row.land_id)

    if source_land_id and db_row.land_id and source_land_id == db_row.land_id:
        score += 10000
        methods.append("exact_land_id")

    overlap = len(source_ids.intersection(db_ids))
    if overlap:
        score += 100 * overlap
        methods.append(f"id_list_overlap:{overlap}")

    if source_land_id_int is not None and source_land_id_int in db_ids:
        score += 250
        methods.append("source_id_in_db_id_list")

    if db_land_id_int is not None and db_land_id_int in source_ids:
        score += 150
        methods.append("db_land_id_in_source_id_list")

    ns = name_score(source.full_name, db_row.full_name)
    if ns:
        score += ns
        methods.append("name_match")

    source_count = len(source.id_list)
    db_count = len(db_row.id_list) or db_row.owned_lands or 0
    if source_count and db_count:
        if source_count == db_count:
            score += 25
            methods.append("same_list_length")
        else:
            score -= min(abs(source_count - db_count), 25)

    if source.lands_count is not None and db_row.owned_lands is not None:
        if source.lands_count == db_row.owned_lands:
            score += 10
            methods.append("same_owned_lands")

    return score, "+".join(methods) if methods else "no_match", overlap


def find_best_match(
    source: SourceRecord,
    by_land_id: Dict[str, List[DbOwnerRow]],
    by_parcel_id: Dict[int, List[DbOwnerRow]],
) -> Optional[MatchResult]:
    candidates: Dict[str, DbOwnerRow] = {}

    for row in by_land_id.get(source.source_land_id, []):
        candidates[row.unique_id] = row

    for parcel_id in source.id_list:
        for row in by_parcel_id.get(parcel_id, []):
            candidates[row.unique_id] = row

    if not candidates:
        return None

    scored: List[Tuple[int, str, int, DbOwnerRow]] = []
    for row in candidates.values():
        score, method, overlap = score_candidate(source, row)
        if score >= MIN_MATCH_SCORE:
            scored.append((score, method, overlap, row))

    if not scored:
        return None

    scored.sort(key=lambda x: (x[0], x[2], x[3].unique_id), reverse=True)
    best_score, best_method, best_overlap, best_row = scored[0]

    tied = [item for item in scored if item[0] == best_score and item[2] == best_overlap]
    ambiguous = len(tied) > 1
    ambiguous_ids = ", ".join(sorted(t[3].unique_id for t in tied)) if ambiguous else ""

    return MatchResult(
        db_row=best_row,
        score=best_score,
        method=best_method,
        overlap_count=best_overlap,
        ambiguous=ambiguous,
        ambiguous_unique_ids=ambiguous_ids,
    )


