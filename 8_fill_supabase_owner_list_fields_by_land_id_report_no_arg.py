def process_table_for_county(
    client: Client,
    county_meta: CountyMeta,
    county_name: str,
    table_name: str,
    sources: List[SourceRecord],
    source_skips: List[SkippedRow],
    source_stats: Dict[str, int],
) -> Tuple[CountyTableReportRow, List[SkippedRow], List[ProposedUpdate]]:
    db_rows = fetch_table_rows_for_county(client, table_name, county_meta.county_id)
    by_land_id, by_parcel_id = build_db_indices(db_rows)

    table_skips: List[SkippedRow] = []
    proposed_updates: List[ProposedUpdate] = []
    matched_sources = 0
    unmatched_sources = 0
    ambiguous_sources = 0
    no_change_matches = 0

    log(f"[MATCH:{table_name}] Matching {len(sources):,} source row(s) for {county_name}...")
    for idx, source in enumerate(sources, start=1):
        if idx == 1 or idx % 500 == 0 or idx == len(sources):
            log(f"[MATCH:{table_name}] {idx:,}/{len(sources):,}")

        match = find_best_match(source, by_land_id, by_parcel_id)
        if match is None:
            unmatched_sources += 1
            table_skips.append(SkippedRow(
                county_name=county_name,
                table_name=table_name,
                source_file=source.source_file,
                source_row_number=source.source_row_number,
                source_land_id=source.source_land_id,
                source_id_list=json_dumps_compact(source.id_list),
                reason="no_matching_db_row",
                details="No DB row matched by land_id or id_list overlap for this county/table.",
            ))
            continue

        if match.ambiguous and SKIP_AMBIGUOUS_MATCHES:
            ambiguous_sources += 1
            table_skips.append(SkippedRow(
                county_name=county_name,
                table_name=table_name,
                source_file=source.source_file,
                source_row_number=source.source_row_number,
                source_land_id=source.source_land_id,
                source_id_list=json_dumps_compact(source.id_list),
                reason="ambiguous_match_skipped",
                details=f"score={match.score}; candidates={match.ambiguous_unique_ids}",
            ))
            continue

        matched_sources += 1
        update = build_update_payload(source, match.db_row, match)
        if update is None:
            no_change_matches += 1
            continue
        proposed_updates.append(update)

    proposed_updates, duplicate_conflicts, duplicate_skips = resolve_duplicate_updates(proposed_updates)
    table_skips.extend(duplicate_skips)

    log(
        f"[MATCH:{table_name}] matched={matched_sources:,} unmatched={unmatched_sources:,} "
        f"ambiguous={ambiguous_sources:,} no_change={no_change_matches:,} updates={len(proposed_updates):,}"
    )

    applied = update_rows_one_by_one(client, table_name, county_meta.county_id, proposed_updates)

    def count_field_updates(field: str) -> int:
        return sum(1 for u in proposed_updates if field in [f.strip() for f in u.fields_changed.split(",")])

    report = CountyTableReportRow(
        county_name=county_name,
        table_name=table_name,
        files_read=source_stats.get("files_read", 0),
        source_rows=source_stats.get("source_rows", 0),
        prepared_sources=len(sources),
        source_rows_missing_land_id=source_stats.get("source_rows_missing_land_id", 0),
        source_rows_without_valid_ids=source_stats.get("source_rows_without_valid_ids", 0),
        source_rows_with_length_mismatch=source_stats.get("source_rows_with_length_mismatch", 0),
        db_rows_fetched=len(db_rows),
        matched_sources=matched_sources,
        unmatched_sources=unmatched_sources,
        ambiguous_sources=ambiguous_sources,
        no_change_matches=no_change_matches,
        duplicate_update_conflicts=duplicate_conflicts,
        proposed_updates=len(proposed_updates),
        applied_updates=applied,
        ids_added_to_db_lists=sum(u.ids_added_to_db_list for u in proposed_updates),
        id_list_updates=count_field_updates("id_list"),
        acres_list_updates=count_field_updates("acres_list"),
        market_value_updates=count_field_updates("market_value_list"),
        owner_tax_year_updates=count_field_updates("owner_tax_year_list"),
        deed_date_updates=count_field_updates("deed_date_list"),
        non_null_acres=sum(u.non_null_acres for u in proposed_updates),
        non_null_market_values=sum(u.non_null_market_values for u in proposed_updates),
        non_null_owner_tax_years=sum(u.non_null_owner_tax_years for u in proposed_updates),
        non_null_deed_dates=sum(u.non_null_deed_dates for u in proposed_updates),
        status="ok",
    )

    return report, table_skips, proposed_updates


