def value_is_present(value: Any) -> bool:
    return value is not None and not is_missing(value)


def normalize_existing_list_for_ids(values: List[Any], ids: List[int]) -> Dict[int, Any]:
    out: Dict[int, Any] = {}
    values, _, _ = pad_or_trim(list(values), len(ids))
    for idx, parcel_id in enumerate(ids):
        if parcel_id not in out:
            out[parcel_id] = values[idx] if idx < len(values) else None
        elif not value_is_present(out[parcel_id]) and idx < len(values):
            out[parcel_id] = values[idx]
    return out


def source_mapping(source: SourceRecord, field: str) -> Dict[int, Any]:
    values = getattr(source, field)
    values, _, _ = pad_or_trim(list(values), len(source.id_list))
    out: Dict[int, Any] = {}
    for idx, parcel_id in enumerate(source.id_list):
        val = values[idx] if idx < len(values) else None
        if parcel_id not in out:
            out[parcel_id] = val
        elif not value_is_present(out[parcel_id]) and value_is_present(val):
            out[parcel_id] = val
    return out


def numeric_equal(a: Any, b: Any) -> bool:
    if not value_is_present(a) and not value_is_present(b):
        return True
    if not value_is_present(a) or not value_is_present(b):
        return False
    try:
        return abs(float(a) - float(b)) < 1e-9
    except Exception:
        return clean_scalar_text(a) == clean_scalar_text(b)


def values_equal(field: str, a: Any, b: Any) -> bool:
    if field in {"acres_list", "market_value_list"}:
        return numeric_equal(a, b)
    if field == "owner_tax_year_list":
        return parse_integer_value(a) == parse_integer_value(b)
    if field == "deed_date_list":
        return parse_date_value(a) == parse_date_value(b)
    return a == b


def lists_equal(field: str, a: List[Any], b: List[Any]) -> bool:
    if len(a) != len(b):
        return False
    return all(values_equal(field, x, y) for x, y in zip(a, b))


def final_value(existing_value: Any, source_value: Any) -> Any:
    if OVERWRITE_EXISTING_VALUES:
        return source_value if value_is_present(source_value) else existing_value
    return existing_value if value_is_present(existing_value) else source_value


def build_update_payload(source: SourceRecord, db_row: DbOwnerRow, match: MatchResult) -> Optional[ProposedUpdate]:
    db_ids = list(db_row.id_list)
    source_ids = list(source.id_list)

    final_ids: List[int] = []
    seen: Set[int] = set()

    for parcel_id in db_ids:
        if parcel_id not in seen:
            final_ids.append(parcel_id)
            seen.add(parcel_id)

    if APPEND_MISSING_INPUT_IDS_TO_EXISTING_ID_LIST:
        for parcel_id in source_ids:
            if parcel_id not in seen:
                final_ids.append(parcel_id)
                seen.add(parcel_id)

    if not final_ids:
        final_ids = source_ids

    if not final_ids:
        return None

    payload: Dict[str, Any] = {}
    fields_changed: List[str] = []

    if db_ids != final_ids:
        payload["id_list"] = final_ids
        fields_changed.append("id_list")

    ids_added = len([i for i in source_ids if i not in set(db_ids)])

    existing_maps: Dict[str, Dict[int, Any]] = {
        "acres_list": normalize_existing_list_for_ids(db_row.acres_list, db_ids),
        "market_value_list": normalize_existing_list_for_ids(db_row.market_value_list, db_ids),
        "owner_tax_year_list": normalize_existing_list_for_ids(db_row.owner_tax_year_list, db_ids),
        "deed_date_list": normalize_existing_list_for_ids(db_row.deed_date_list, db_ids),
    }

    # If DB id_list is empty but DB land_id/acres exists, let acres participate in the merge.
    db_land_id_int = parse_integer_value(db_row.land_id)
    if not db_ids and db_land_id_int is not None and db_row.acres is not None:
        existing_maps["acres_list"][db_land_id_int] = db_row.acres

    source_maps: Dict[str, Dict[int, Any]] = {
        field: source_mapping(source, field)
        for field in VALUE_LIST_FIELDS
    }

    final_lists: Dict[str, List[Any]] = {}
    for field in VALUE_LIST_FIELDS:
        values: List[Any] = []
        for parcel_id in final_ids:
            existing_val = existing_maps[field].get(parcel_id)
            source_val = source_maps[field].get(parcel_id)
            values.append(final_value(existing_val, source_val))
        final_lists[field] = values

        # Do not write completely empty optional value arrays, unless existing had values and list shape changed.
        existing_aligned = [existing_maps[field].get(parcel_id) for parcel_id in final_ids]
        has_any_value = any(value_is_present(v) for v in values)
        existing_has_any_value = any(value_is_present(v) for v in existing_aligned)

        if has_any_value and not lists_equal(field, existing_aligned, values):
            payload[field] = values
            fields_changed.append(field)
        elif field == "acres_list" and not existing_has_any_value and has_any_value:
            payload[field] = values
            fields_changed.append(field)

    if not payload:
        return None

    return ProposedUpdate(
        county_name=source.county_name,
        table_name=db_row.table_name,
        unique_id=db_row.unique_id,
        source_file=source.source_file,
        source_row_number=source.source_row_number,
        source_land_id=source.source_land_id,
        source_id_list_json=json_dumps_compact(source.id_list),
        db_land_id=db_row.land_id,
        db_id_list_json=json_dumps_compact(db_row.id_list),
        final_id_list_json=json_dumps_compact(final_ids),
        match_method=match.method,
        match_score=match.score,
        overlap_count=match.overlap_count,
        fields_changed=", ".join(fields_changed),
        ids_added_to_db_list=ids_added,
        non_null_acres=non_null_count(final_lists["acres_list"]),
        non_null_market_values=non_null_count(final_lists["market_value_list"]),
        non_null_owner_tax_years=non_null_count(final_lists["owner_tax_year_list"]),
        non_null_deed_dates=non_null_count(final_lists["deed_date_list"]),
        payload_fields=payload,
    )


def resolve_duplicate_updates(updates: List[ProposedUpdate]) -> Tuple[List[ProposedUpdate], int, List[SkippedRow]]:
    """If multiple source rows target the same DB unique_id, keep the strongest one."""
    by_uid: Dict[str, List[ProposedUpdate]] = {}
    for upd in updates:
        by_uid.setdefault(upd.unique_id, []).append(upd)

    resolved: List[ProposedUpdate] = []
    skipped: List[SkippedRow] = []
    duplicate_conflicts = 0

    for uid, group in by_uid.items():
        if len(group) == 1:
            resolved.append(group[0])
            continue
        duplicate_conflicts += len(group) - 1
        group.sort(
            key=lambda u: (
                u.match_score,
                u.overlap_count,
                len((u.fields_changed or "").split(",")),
                u.source_row_number,
            ),
            reverse=True,
        )
        winner = group[0]
        resolved.append(winner)
        for loser in group[1:]:
            skipped.append(SkippedRow(
                county_name=loser.county_name,
                table_name=loser.table_name,
                source_file=loser.source_file,
                source_row_number=loser.source_row_number,
                source_land_id=loser.source_land_id,
                source_id_list=loser.source_id_list_json,
                reason="duplicate_update_same_unique_id_lost",
                details=f"unique_id={uid}; kept source row {winner.source_row_number} with score {winner.match_score}.",
            ))

    return resolved, duplicate_conflicts, skipped


