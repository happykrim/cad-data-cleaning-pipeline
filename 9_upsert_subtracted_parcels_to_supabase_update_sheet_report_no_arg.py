def ensure_folder(path: Path) -> None:
    path.mkdir(parents=True, exist_ok=True)


def is_blank(value: Any) -> bool:
    if value is None:
        return True
    if isinstance(value, float):
        try:
            if math.isnan(value):
                return True
        except Exception:
            pass
    text = str(value).strip()
    return text == "" or text.lower() in {"nan", "none", "null", "nat"}


def clean_text(value: Any, *, max_len: Optional[int] = None) -> Optional[str]:
    if is_blank(value):
        return None
    text = re.sub(r"\s+", " ", str(value).strip())
    if max_len is not None and len(text) > max_len:
        return text[:max_len]
    return text


def normalize_name(name: str) -> str:
    return re.sub(r"[^a-z0-9]+", "", (name or "").strip().lower())


def parse_decimal(value: Any) -> Optional[Decimal]:
    if is_blank(value):
        return None
    text = str(value).strip().replace(",", "")
    if text.startswith("$"):
        text = text[1:]
    try:
        return Decimal(text)
    except (InvalidOperation, ValueError):
        return None


def parse_int(value: Any) -> Optional[int]:
    if is_blank(value):
        return None
    text = str(value).strip().replace(",", "")
    try:
        return int(Decimal(text))
    except Exception:
        return None


def parse_jsonish_list(value: Any) -> List[Any]:
    if value is None:
        return []
    if isinstance(value, list):
        return value
    if isinstance(value, tuple):
        return list(value)
    if isinstance(value, float):
        try:
            if math.isnan(value):
                return []
        except Exception:
            pass
    text = str(value).strip()
    if not text or text.lower() in {"nan", "none", "null", "nat"}:
        return []
    if text.startswith("[") and text.endswith("]"):
        try:
            parsed = json.loads(text)
            if isinstance(parsed, list):
                return parsed
        except Exception:
            pass
        try:
            parsed = ast.literal_eval(text)
            if isinstance(parsed, list):
                return parsed
        except Exception:
            pass
    if "|" in text:
        return [p.strip() for p in text.split("|")]
    return [p.strip() for p in text.split(",")]


def pad_or_trim(values: List[Any], target_len: int, pad_value: Any = None) -> List[Any]:
    if target_len <= 0:
        return []
    out = list(values[:target_len])
    while len(out) < target_len:
        out.append(pad_value)
    return out


def parse_int_list(value: Any, target_len: Optional[int] = None) -> List[Optional[int]]:
    raw_items = parse_jsonish_list(value)
    out: List[Optional[int]] = []
    for item in raw_items:
        out.append(parse_int(item))
    if target_len is not None:
        out = pad_or_trim(out, target_len, None)
    return out


def parse_decimal_list(value: Any, target_len: Optional[int] = None) -> List[Optional[Decimal]]:
    raw_items = parse_jsonish_list(value)
    out: List[Optional[Decimal]] = []
    for item in raw_items:
        out.append(parse_decimal(item))
    if target_len is not None:
        out = pad_or_trim(out, target_len, None)
    return out


def parse_date_to_iso(value: Any) -> Optional[str]:
    if is_blank(value):
        return None
    text = str(value).strip()
    # Pandas handles MM/DD/YYYY, YYYY-MM-DD, and Excel-like strings well enough here.
    dt = pd.to_datetime(text, errors="coerce")
    if pd.isna(dt):
        return None
    return dt.strftime("%Y-%m-%d")


def parse_date_list(value: Any, target_len: Optional[int] = None) -> List[Optional[str]]:
    raw_items = parse_jsonish_list(value)
    out = [parse_date_to_iso(item) for item in raw_items]
    if target_len is not None:
        out = pad_or_trim(out, target_len, None)
    return out


def parse_bool(value: Any) -> Optional[bool]:
    if is_blank(value):
        return None
    text = str(value).strip().lower()
    if text in {"true", "t", "1", "yes", "y"}:
        return True
    if text in {"false", "f", "0", "no", "n"}:
        return False
    return None


def parse_bool_list(value: Any, target_len: Optional[int] = None) -> List[Optional[bool]]:
    raw_items = parse_jsonish_list(value)
    out = [parse_bool(item) for item in raw_items]
    if target_len is not None:
        out = pad_or_trim(out, target_len, None)
    return out


def normalize_numeric_array(values: Optional[List[Optional[Decimal]]]) -> Optional[List[Optional[float]]]:
    if not values:
        return None
    return [float(v) if v is not None else None for v in values]


def normalize_int_array(values: Optional[List[Optional[int]]]) -> Optional[List[Optional[int]]]:
    if not values:
        return None
    return [int(v) if v is not None else None for v in values]


def normalize_date_array(values: Optional[List[Optional[str]]]) -> Optional[List[Optional[str]]]:
    if not values:
        return None
    return [v if v else None for v in values]


def naive_split_full_name(full_name: Optional[Any]) -> Tuple[Optional[str], Optional[str]]:
    if is_blank(full_name):
        return None, None
    text = str(full_name).strip()
    # Remove MA# prefix for grouped mailing-address rows.
    if text.upper().startswith("MA#") and "|" in text:
        text = text.split("|", 1)[1].strip()
    if "&" in text:
        text = text.split("&", 1)[0].strip()
    tokens = text.split()
    if not tokens:
        return None, None
    if len(tokens) == 1:
        return tokens[0][:100], None
    # This follows the reference script behavior: first token as last_name, rest as first_name.
    return " ".join(tokens[1:])[:100], tokens[0][:100]


