def main() -> None:
    ensure_folder(REPORT_FOLDER)
    log("=" * 90)
    log("[START] Final upsert of subtracted parcel-owner rows to Supabase")
    log(f"[TIME]   {RUN_TS.strftime('%Y-%m-%d %H:%M:%S')}")
    log(f"[INPUT]  {INPUT_FOLDER}")
    log(f"[REPORT] {REPORT_FOLDER}")
    log(f"[MODE]   {'DRY RUN' if DRY_RUN else 'LIVE RUN'}")
    log(f"[DB]     {UNIQUE_OWNER_TABLE}")
    log("=" * 90)

    client = get_supabase_client()
    county_meta_map = fetch_county_metadata(client)

    log("[GSHEET] Opening Sheet1...")
    sheet1_ws = None
    sheet_index: Dict[str, List[int]] = {}
    sheet_hmap: Dict[str, int] = {}
    if DRY_RUN:
        log("[GSHEET] Dry run enabled: Sheet1 will not be updated.")
    else:
        gclient = get_gspread_client(GSHEET_CREDENTIALS_JSON, SCOPE)
        sheet1_ws = open_worksheet(gclient, GSHEET_URL, SHEET1_NAME)
        sheet_index, sheet_hmap = build_sheet1_index(sheet1_ws)
        log(f"[GSHEET] Loaded Sheet1 index for {len(sheet_index):,} county names.")

    table_rows_before = get_table_row_count(client)
    log(f"[DB] Table rows before run: {table_rows_before:,}")

    county_folders = list_county_folders(INPUT_FOLDER)
    log(f"[MAIN] Found {len(county_folders):,} county folder(s).")

    county_reports: List[CountyReportRow] = []
    skipped_rows_all: List[Dict[str, Any]] = []
    inserted_audit_all: List[Dict[str, Any]] = []
    rows_remaining_for_test = TEST_ROW_LIMIT if TEST_MODE else None

    for county_idx, county_folder in enumerate(county_folders, start=1):
        county_name = county_folder.name
        log("=" * 90)
        log(f"[COUNTY] ({county_idx}/{len(county_folders)}) {county_name}")
        meta = county_meta_map.get(normalize_name(county_name))
        if not meta:
            error = "county_not_found_in_supabase_county_table"
            log(f"[WARN] {county_name}: {error}")
            county_reports.append(CountyReportRow(
                county_name=county_name,
                county_id=None,
                source_files=0,
                source_rows=0,
                rows_attempted=0,
                rows_added=0,
                eap_added_to_db=0,
                last_index_before=0,
                max_index_after=0,
                db_count_before=0,
                db_count_after=0,
                sheet_rows_updated=0,
                status="warn",
                error=error,
            ))
            continue

        try:
            db_count_before = get_count_for_county(client, meta.county_id)
            last_index = get_max_index_for_county(client, meta.county_id)
            log(f"[DB] county_id={meta.county_id} rows_before={db_count_before:,} last_index={last_index:,}")

            owner_rows, source_rows, skipped_rows, file_names = build_owner_rows_for_county(
                county_folder,
                meta,
                start_index=last_index,
            )
            skipped_rows_all.extend(skipped_rows)
            log(f"[BUILD] source_rows={source_rows:,} valid_rows={len(owner_rows):,} skipped_rows={len(skipped_rows):,}")

            if TEST_MODE and owner_rows:
                if rows_remaining_for_test is None or rows_remaining_for_test <= 0:
                    log("[TEST] Reached TEST_ROW_LIMIT. Skipping remaining counties.")
                    break
                if len(owner_rows) > rows_remaining_for_test:
                    log(f"[TEST] Truncating rows from {len(owner_rows):,} to {rows_remaining_for_test:,}")
                    owner_rows = owner_rows[:rows_remaining_for_test]
                rows_remaining_for_test -= len(owner_rows)

            eap_added_count = sum(row.eap_true_count for row in owner_rows)
            affected = 0
            if owner_rows and not DRY_RUN:
                affected = upsert_owner_rows(client, owner_rows)
                log(f"[DB] Upsert complete. attempted={len(owner_rows):,} affected_response_rows={affected:,}")
            elif owner_rows:
                log(f"[DRY RUN] Would upsert {len(owner_rows):,} row(s).")
            else:
                log("[MAIN] No valid rows to upsert for this county.")

            db_count_after = get_count_for_county(client, meta.county_id) if not DRY_RUN else db_count_before
            rows_added = len(owner_rows) if DRY_RUN else max(db_count_after - db_count_before, 0)
            max_index_after = get_max_index_for_county(client, meta.county_id) if not DRY_RUN else last_index + len(owner_rows)

            sheet_rows_updated = 0
            if not DRY_RUN and sheet1_ws is not None:
                sheet_rows_updated = update_sheet1_final_db_columns(
                    sheet1_ws,
                    sheet_index,
                    sheet_hmap,
                    county_name=meta.county_name,
                    eap_added_to_db=eap_added_count,
                    all_added_to_db=rows_added,
                    run_date=RUN_TS.strftime("%Y-%m-%d"),
                )
                log(
                    f"[GSHEET] Updated {sheet_rows_updated:,} Sheet1 row(s): "
                    f"EAP Added to DB={eap_added_count:,}, All Added to DB={rows_added:,}, "
                    f"All Added to DB Date={RUN_TS.strftime('%Y-%m-%d')}"
                )
            elif DRY_RUN:
                log(
                    f"[DRY RUN] Would update Sheet1: EAP Added to DB={eap_added_count:,}, "
                    f"All Added to DB={rows_added:,}, All Added to DB Date={RUN_TS.strftime('%Y-%m-%d')}"
                )

            inserted_audit_all.extend(build_inserted_audit_rows(owner_rows))
            county_reports.append(CountyReportRow(
                county_name=meta.county_name,
                county_id=meta.county_id,
                source_files=len(file_names),
                source_rows=source_rows,
                rows_attempted=len(owner_rows),
                rows_added=rows_added,
                eap_added_to_db=eap_added_count,
                last_index_before=last_index,
                max_index_after=max_index_after,
                db_count_before=db_count_before,
                db_count_after=db_count_after,
                sheet_rows_updated=sheet_rows_updated,
                status="ok",
                error=None,
            ))
            log(f"[DONE] {county_name}: added={rows_added:,}, eap_added={eap_added_count:,}")

        except Exception as exc:
            err = f"{type(exc).__name__}: {exc}"
            log(f"[ERROR] {county_name}: {err}")
            traceback.print_exc()
            county_reports.append(CountyReportRow(
                county_name=county_name,
                county_id=meta.county_id,
                source_files=0,
                source_rows=0,
                rows_attempted=0,
                rows_added=0,
                eap_added_to_db=0,
                last_index_before=0,
                max_index_after=0,
                db_count_before=0,
                db_count_after=0,
                sheet_rows_updated=0,
                status="error",
                error=err,
            ))

    table_rows_after = get_table_row_count(client) if not DRY_RUN else table_rows_before

    summary_rows = [asdict(row) for row in county_reports]
    write_csv_report(REPORT_SUMMARY_CSV_PATH, summary_rows)
    write_csv_report(REPORT_INSERTED_AUDIT_CSV_PATH, inserted_audit_all)
    write_csv_report(REPORT_SKIPPED_CSV_PATH, skipped_rows_all)
    generate_html_report(REPORT_HTML_PATH, table_rows_before, table_rows_after, county_reports, skipped_rows_all)

    total_added = sum(row.rows_added for row in county_reports)
    total_eap = sum(row.eap_added_to_db for row in county_reports)
    log("=" * 90)
    log("[DONE] Final Supabase upsert pipeline completed.")
    log(f"Counties processed : {len(county_reports):,}")
    log(f"Rows added         : {total_added:,}")
    log(f"EAP added to DB    : {total_eap:,}")
    log(f"Skipped rows       : {len(skipped_rows_all):,}")
    log(f"Report folder      : {REPORT_FOLDER}")
    log("=" * 90)


if __name__ == "__main__":
    main()
