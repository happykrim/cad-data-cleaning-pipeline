def get_supabase_client(env_path: Optional[str] = None) -> Client:
    if create_client is None:
        raise RuntimeError("The 'supabase' package is not installed. Run: pip install supabase")
    if env_path is None:
        default_env = Path(__file__).resolve().parent / ".env"
        if default_env.exists():
            load_dotenv(default_env, override=False)
        else:
            load_dotenv(find_dotenv(usecwd=True), override=False)
    else:
        load_dotenv(env_path, override=False)

    url = os.getenv("SUPABASE_URL")
    key = os.getenv("SUPABASE_SERVICE_ROLE_KEY")
    if not url or not key:
        raise RuntimeError("Missing SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY in environment/.env")
    return create_client(url, key)


def fetch_county_metadata(client: Client) -> Dict[str, CountyMeta]:
    log("[META] Fetching county metadata from Supabase...")
    resp = _retry(
        client.table(COUNTY_TABLE)
        .select("county_id, county_name, cad_category")
        .execute
    )
    rows = resp.data or []
    out: Dict[str, CountyMeta] = {}
    for row in rows:
        name = clean_text(row.get("county_name"))
        if not name:
            continue
        out[normalize_name(name)] = CountyMeta(
            county_id=int(row["county_id"]),
            county_name=name,
            cad_category=row.get("cad_category"),
        )
    log(f"[META] Loaded {len(out):,} county records.")
    return out


def get_table_row_count(client: Client) -> int:
    resp = _retry(
        client.table(UNIQUE_OWNER_TABLE)
        .select("unique_id", count="exact")
        .execute
    )
    return int(resp.count or 0)


def get_count_for_county(client: Client, county_id: int) -> int:
    resp = _retry(
        client.table(UNIQUE_OWNER_TABLE)
        .select("unique_id", count="exact")
        .eq("county_id", county_id)
        .execute
    )
    return int(resp.count or 0)


def extract_index_from_unique_id(unique_id: str) -> Optional[int]:
    try:
        parts = str(unique_id).split("-")
        if len(parts) != 4:
            return None
        return int(parts[2])
    except Exception:
        return None


def get_max_index_for_county(client: Client, county_id: int) -> int:
    offset = 0
    max_index = 0
    while True:
        resp = _retry(
            client.table(UNIQUE_OWNER_TABLE)
            .select("unique_id")
            .eq("county_id", county_id)
            .range(offset, offset + FETCH_PAGE_SIZE - 1)
            .execute
        )
        rows = resp.data or []
        if not rows:
            break
        for row in rows:
            idx = extract_index_from_unique_id(row.get("unique_id", ""))
            if idx is not None and idx > max_index:
                max_index = idx
        if len(rows) < FETCH_PAGE_SIZE:
            break
        offset += FETCH_PAGE_SIZE
    return max_index


def generate_unique_id(county_id: int, index: int, version: int = 1) -> str:
    return f"TX-{county_id}-{index}-{version}"


