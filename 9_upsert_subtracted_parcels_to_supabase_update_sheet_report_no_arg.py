def list_county_folders(input_folder: Path) -> List[Path]:
    if not input_folder.exists():
        raise FileNotFoundError(f"Input folder does not exist: {input_folder}")
    if not input_folder.is_dir():
        raise NotADirectoryError(f"Input path is not a folder: {input_folder}")
    folders = [p for p in input_folder.iterdir() if p.is_dir()]
    if SELECTED_COUNTIES:
        selected = {normalize_name(c) for c in SELECTED_COUNTIES}
        folders = [p for p in folders if normalize_name(p.name) in selected]
    return sorted(folders, key=lambda p: p.name)


def list_data_files(county_folder: Path) -> List[Path]:
    files = [p for p in county_folder.iterdir() if p.is_file() and p.suffix.lower() in SUPPORTED_SUFFIXES]
    # Prefer final CSVs over reports/audits if multiple files are present.
    files = [p for p in files if not p.name.lower().startswith(("summary", "audit"))]
    return sorted(files)


def read_data_file(path: Path) -> pd.DataFrame:
    suffix = path.suffix.lower()
    if suffix == ".csv":
        return pd.read_csv(path, **CSV_READ_KWARGS)
    if suffix in {".xlsx", ".xls", ".xlsm"}:
        return pd.read_excel(path, engine="openpyxl", dtype=str, keep_default_na=False)
    raise ValueError(f"Unsupported file type: {path.suffix}")


def read_county_files(county_folder: Path) -> Tuple[pd.DataFrame, int, List[str]]:
    files = list_data_files(county_folder)
    if not files:
        return pd.DataFrame(), 0, []
    dfs: List[pd.DataFrame] = []
    total_rows = 0
    file_names: List[str] = []
    for path in files:
        log(f"[FILE] Reading {county_folder.name}/{path.name}")
        df = read_data_file(path)
        df["__source_file"] = path.name
        df["__source_row_number"] = range(2, len(df) + 2)
        dfs.append(df)
        total_rows += len(df)
        file_names.append(path.name)
    full_df = pd.concat(dfs, ignore_index=True) if dfs else pd.DataFrame()
    return full_df, total_rows, file_names


