def owner_row_to_payload(row: OwnerRow) -> Dict[str, Any]:
    return {
        "unique_id": row.unique_id,
        "county_id": row.county_id,
        "county_name": row.county_name,
        "land_id": row.land_id,
        "full_name": row.full_name,
        "legal_address": row.legal_address,
        "mailing_address": row.mailing_address,
        "acres": float(row.acres) if row.acres is not None else None,
        "city": row.city,
        "zip": row.zip,
        "first_name_1": row.first_name_1,
        "last_name_1": row.last_name_1,
        "owned_lands": int(row.owned_lands or 0),
        "cad_category": row.cad_category,
        "acres_list": normalize_numeric_array(row.acres_list),
        "id_list": row.id_list if row.id_list else None,
        "market_value_list": normalize_numeric_array(row.market_value_list),
        "owner_tax_year_list": normalize_int_array(row.owner_tax_year_list),
        "deed_date_list": normalize_date_array(row.deed_date_list),
    }


def upsert_owner_rows(client: Client, rows: List[OwnerRow]) -> int:
    if not rows:
        return 0
    payloads = [owner_row_to_payload(row) for row in rows]
    affected_total = 0
    for i in range(0, len(payloads), CHUNK_SIZE):
        chunk = payloads[i:i + CHUNK_SIZE]
        log(f"[DB] Upserting rows {i + 1:,}-{i + len(chunk):,} / {len(payloads):,}")
        resp = _retry(
            client.table(UNIQUE_OWNER_TABLE)
            .upsert(chunk, on_conflict="unique_id")
            .execute
        )
        affected_total += len(resp.data or [])
    return affected_total


