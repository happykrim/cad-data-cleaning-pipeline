def build_owner_rows_for_county(
    county_folder: Path,
    county_meta: CountyMeta,
    start_index: int,
) -> Tuple[List[OwnerRow], int, List[Dict[str, Any]], List[str]]:
    df, source_rows, file_names = read_county_files(county_folder)
    skipped: List[Dict[str, Any]] = []
    if df.empty:
        return [], source_rows, skipped, file_names

    missing = [c for c in REQUIRED_COLUMNS if c not in df.columns]
    if missing:
        raise KeyError(f"Missing required columns in {county_folder.name}: {missing}")

    df = df.reset_index(drop=True)
    owner_rows: List[OwnerRow] = []

    for idx, row in df.iterrows():
        if (idx + 1) % PROGRESS_EVERY_ROWS == 0:
            log(f"[BUILD] {county_folder.name}: built {idx + 1:,}/{len(df):,} source rows...")

        land_id = clean_text(row.get(COL_ID), max_len=64)
        local_id_int = parse_int(row.get(COL_ID))

        raw_id_list = parse_int_list(row.get(COL_ID_LIST)) if COL_ID_LIST in row.index else []
        id_list = [int(v) for v in raw_id_list if v is not None]
        if not id_list and local_id_int is not None:
            id_list = [local_id_int]

        if not land_id and id_list:
            land_id = str(id_list[0])

        if not land_id and not id_list:
            skipped.append({
                "county_name": county_folder.name,
                "source_file": row.get("__source_file"),
                "source_row_number": row.get("__source_row_number"),
                "reason": "missing_id_and_id_list",
            })
            continue

        target_len = len(id_list) if id_list else 1
        acres_list = parse_decimal_list(row.get(COL_ACRES_LIST), target_len=target_len) if COL_ACRES_LIST in row.index else []
        if not any(v is not None for v in acres_list):
            acres_val = parse_decimal(row.get(COL_ACRES))
            acres_list = [acres_val] if target_len == 1 else pad_or_trim([acres_val], target_len, None)

        market_value_list = parse_decimal_list(row.get(COL_MARKET_VALUE_LIST), target_len=target_len) if COL_MARKET_VALUE_LIST in row.index else None
        owner_tax_year_list = parse_int_list(row.get(COL_OWNER_TAX_YEAR_LIST), target_len=target_len) if COL_OWNER_TAX_YEAR_LIST in row.index else None
        deed_date_list = parse_date_list(row.get(COL_DEED_DATE_LIST), target_len=target_len) if COL_DEED_DATE_LIST in row.index else None
        eap_flags = parse_bool_list(row.get(COL_LEGAL_ACREAGE_FILLED_BY_SCRIPT), target_len=target_len) if COL_LEGAL_ACREAGE_FILLED_BY_SCRIPT in row.index else []
        eap_true_count = sum(1 for flag in eap_flags if flag is True)

        full_name = clean_text(row.get(COL_FULL_NAME), max_len=150)
        legal_address = clean_text(row.get(COL_LEGAL_ADDRESS))
        mailing_address = clean_text(row.get(COL_MAILING_ADDRESS))
        acres = parse_decimal(row.get(COL_ACRES))
        city = clean_text(row.get(COL_CITY), max_len=100)
        zip_code = clean_text(row.get(COL_ZIP), max_len=10)
        first_name_1, last_name_1 = naive_split_full_name(full_name)
