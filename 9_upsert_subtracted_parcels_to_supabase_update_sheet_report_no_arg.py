def get_gspread_client(credentials_json_file: str, scope: List[str]):
    if gspread is None or ServiceAccountCredentials is None:
        raise RuntimeError("Google Sheets packages are missing. Run: pip install gspread oauth2client")
    creds_path = Path(credentials_json_file)
    if not creds_path.exists():
        alt = Path(__file__).resolve().parent / credentials_json_file
        if alt.exists():
            creds_path = alt
    creds = ServiceAccountCredentials.from_json_keyfile_name(str(creds_path), scope)
    return gspread.authorize(creds)


def open_worksheet(client, gsheet_url: str, worksheet_name: str):
    spreadsheet = client.open_by_url(gsheet_url)
    return spreadsheet.worksheet(worksheet_name)


def header_map(headers: Sequence[str]) -> Dict[str, int]:
    return {str(h).strip(): i + 1 for i, h in enumerate(headers) if str(h).strip()}


def build_sheet1_index(ws) -> Tuple[Dict[str, List[int]], Dict[str, int]]:
    values = ws.get_all_values()
    if not values or len(values) < 2:
        raise ValueError("Sheet1 is empty or has no data rows.")
    headers = values[0]
    hmap = header_map(headers)
    county_col = hmap.get(SHEET1_COUNTY_HEADER)
    if not county_col:
        raise ValueError(f'Sheet1 missing header "{SHEET1_COUNTY_HEADER}"')

    county_to_rows: Dict[str, List[int]] = {}
    for row_num in range(2, len(values) + 1):
        row = values[row_num - 1]
        county_val = row[county_col - 1].strip() if len(row) >= county_col else ""
        if county_val:
            county_to_rows.setdefault(normalize_name(county_val), []).append(row_num)
    return county_to_rows, hmap


def get_sheet_col(hmap: Dict[str, int], header_name: str, fallback_col: int) -> int:
    return hmap.get(header_name, fallback_col)


def update_sheet1_final_db_columns(
    ws,
    sheet_index: Dict[str, List[int]],
    hmap: Dict[str, int],
    county_name: str,
    eap_added_to_db: int,
    all_added_to_db: int,
    run_date: str,
) -> int:
    key = normalize_name(county_name)
    target_rows = sheet_index.get(key) or []
    if not target_rows:
        return 0

    eap_col = get_sheet_col(hmap, SHEET1_EAP_ADDED_HEADER, SHEET1_EAP_ADDED_FALLBACK_COL)
    all_col = get_sheet_col(hmap, SHEET1_ALL_ADDED_HEADER, SHEET1_ALL_ADDED_FALLBACK_COL)
    date_col = get_sheet_col(hmap, SHEET1_ALL_ADDED_DATE_HEADER, SHEET1_ALL_ADDED_DATE_FALLBACK_COL)

    updates = []
    for row_num in target_rows:
        updates.extend([
            {"range": gspread.utils.rowcol_to_a1(row_num, eap_col), "values": [[eap_added_to_db]]},
            {"range": gspread.utils.rowcol_to_a1(row_num, all_col), "values": [[all_added_to_db]]},
            {"range": gspread.utils.rowcol_to_a1(row_num, date_col), "values": [[run_date]]},
        ])
    ws.batch_update(updates)
    return len(target_rows)


